world_map = [['.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],
             ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],
             ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],
             ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],
             ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],
             ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],
             ['.', '.', '.', '.', '.', 'i', '.', '.', '.', '.'],
             ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],
             ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],
             ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.']]


def print_map():
    for row in world_map:
        for column in row:
            print(column, end="")
        print()


def move_left():
    for row in world_map:
        for i, column in enumerate(row):
            if column == "i":
                if i > 0 and row[i - 1] == '.':
                    row[i - 1] = 'i'
                    row[i] = '.'


def main():
    move_left()
    print_map()
    move_left()
    print_map()


if __name__ == "__main__":
    main()
